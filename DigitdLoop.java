class DigitdLoop
{
	public static void main(String[] args) {
		System.out.println("**********");
		for(char ch='0';ch<'9';ch++){
			System.out.println(ch + " ");
		}
		System.out.println("**********");
		for(char ch=48;ch<'9';ch++)
		{
			System.out.println(ch + " ");
		}
		System.out.println("**********");
		for(char ch='0';ch<57;ch++){
			System.out.println(ch + " ");
		}
	}
} 